package com.spring.board.vo;

public class ScheduleVo {
   
   private int scheduleNo;
   private String scheduleCity;
   private String scheduleTitle;
   private String startTime;
   private String endTime;
   private String startDate;
   private String endDate;
   private int perLimit;
   private int perApply;
   
   public int getScheduleNo() {
      return scheduleNo;
   }
   public void setScheduleNo(int scheduleNo) {
      this.scheduleNo = scheduleNo;
   }
   public String getScheduleCity() {
      return scheduleCity;
   }
   public void setScheduleCity(String scheduleCity) {
      this.scheduleCity = scheduleCity;
   }
   public String getScheduleTitle() {
      return scheduleTitle;
   }
   public void setScheduleTitle(String scheduleTitle) {
      this.scheduleTitle = scheduleTitle;
   }
   public String getStartTime() {
      return startTime;
   }
   public void setStartTime(String startTime) {
      this.startTime = startTime;
   }
   public String getEndTime() {
      return endTime;
   }
   public void setEndTime(String endTime) {
      this.endTime = endTime;
   }
   public String getStartDate() {
      return startDate;
   }
   public void setStartDate(String startDate) {
      this.startDate = startDate;
   }
   public String getEndDate() {
      return endDate;
   }
   public void setEndDate(String endDate) {
      this.endDate = endDate;
   }
   public int getPerLimit() {
      return perLimit;
   }
   public void setPerLimit(int perLimit) {
      this.perLimit = perLimit;
   }
   public int getPerApply() {
      return perApply;
   }
   public void setPerApply(int perApply) {
      this.perApply = perApply;
   }
   
   
   

}